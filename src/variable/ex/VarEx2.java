package variable.ex;

public class VarEx2 {
    public static void main(String[] args) {
        /*  num1을 선언하고 10을 할당
            num2를 선언하고 20을 할당
            두 변수의 합을 구하고 sum에 저장
            sum 변수의 값을 출력
         */
        int num1 = 10;
        int num2 = 20;
        int sum = num1 + num2;
        System.out.println(sum);
    }
}
