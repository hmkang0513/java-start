package variable.ex;

public class VarEx1 {
    public static void main(String[] args){
        // 반복해서 나오는 숫자 4, 3를 다른 숫자로 한번에 변경할 수 있도록 num1, num2를 사용해서 변경하기
        int num1 = 4;
        int num2 = 3;
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
    }
}
